#!/usr/bin/env python3
"""
Module for converting a float number to its string representation.

Contains:
- to_str(n: float) -> str: Converts a float to a string.
"""


def to_str(n: float) -> str:
    """calling function with built-in function str()"""
    result = str(n)
    """Returned result after the function was called"""
    return result
